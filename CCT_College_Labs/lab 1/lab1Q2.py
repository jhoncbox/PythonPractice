## program to calculate the amount of minutes in one year
import time
print("Calculating the number of minutes in a year...")
time.sleep(1.5)
day = int(365)
hour = int(24)
min = int(60)
TotalMinOnAYear = day * hour * min

print("Days on a Year: " + str(day))
time.sleep(1.5)
print("Hours on a Day: " + str(hour))
time.sleep(1.5)
print("Minutes on a Hour: " + str(min))
time.sleep(1.5)
print("Minutes on a YEAR: " + str(TotalMinOnAYear))
time.sleep(1.5)
